function suma(numero1, numero2) {
  const resultado = numero1 + numero2;
  return resultado;
}

function resta(numero1, numero2) {
  const resultado = numero1 - numero2;
  return resultado;
}

function division(numero1, numero2) {
  if (numero2 == 0) {
    return 0;
  }
  const resultado = numero1 / numero2;
  return resultado;
}

function mult(numero1, numero2) {
  const resultado = numero1 * numero2;
  return resultado;
}

